#include <vector>

int lastRemaining(int n) {
    bool leftToRight = true;
    int remaining = n;
    int head = 1;
    int step = 1;

    while (remaining > 1) {
        if (leftToRight || remaining % 2 == 1) {
            head += step;
        }
        remaining /= 2;
        step *= 2;
        leftToRight = !leftToRight;
    }
    return head;
}